const fs = require('fs');
const path = require('path');
const MFS = require('memory-fs');
const webpack = require('webpack');
const chokidar = require('chokidar');
const devMiddlewareFn = require('webpack-dev-middleware');
const hotMiddlewareFn = require('webpack-hot-middleware');
const clientConfig = require('./webpack.client.config');
const serverConfig = require('./webpack.server.config');

const readFile = (fileSystem, file) => {
    try {
        return fileSystem.readFileSync(path.join(clientConfig.output.path, file), 'utf-8');
    } catch (e) {
        console.log(e);
        return e;
    }
};

module.exports = function setupDevServer(app, templatePath, cb) {
    let bundle;
    let template;
    let clientManifest;

    let ready;
    // export ready promise
    const readyPromise = new Promise((resolve) => { ready = resolve; });
    const update = () => {
    // when bundle and clientManifest were compiled completely, then call resolve method of promise
        if (bundle && clientManifest) {
            ready();
            cb(bundle, {
                template,
                clientManifest,
            });
        }
    };

    // read and watch template from disk
    template = fs.readFileSync(templatePath, 'utf-8');
    chokidar.watch(templatePath).on('change', () => {
        template = fs.readFileSync(templatePath, 'utf-8');
        console.log('✅ index.html template updated');
        update();
    });

    // modify client config to work with hot middleware
    clientConfig.entry.app = ['webpack-hot-middleware/client', clientConfig.entry.app];
    clientConfig.output.filename = '[name].js';
    clientConfig.plugins.push(
        new webpack.HotModuleReplacementPlugin(),
        new webpack.NoEmitOnErrorsPlugin(),
    );

    // dev middleware
    const clientCompiler = webpack(clientConfig);
    const devMiddleware = devMiddlewareFn(clientCompiler, {
        publicPath: clientConfig.output.publicPath,
        noInfo: true,
    });
    app.use(devMiddleware);
    clientCompiler.hooks.done.tap('done', (stats) => {
        const json = stats.toJson();
        json.errors.forEach(err => console.error(err));
        json.warnings.forEach(err => console.warn(err));
        if (json.errors.length) return;
        clientManifest = JSON.parse(readFile(
            devMiddleware.fileSystem,
            'vue-ssr-client-manifest.json',
        ));
        console.log('✅ vue-ssr-client-manifest.json done');
        update();
    });

    // hot middleware
    app.use(hotMiddlewareFn(clientCompiler, { heartbeat: 5000 }));

    // watch and update server renderer
    const serverCompiler = webpack(serverConfig);
    const mfs = new MFS();
    serverCompiler.outputFileSystem = mfs;
    serverCompiler.watch({}, (err, stats) => {
        if (err) throw err;
        const json = stats.toJson();
        if (json.errors.length) return;

        // read bundle generated by vue-ssr-webpack-plugin
        bundle = JSON.parse(readFile(mfs, 'vue-ssr-server-bundle.json'));
        console.log('✅ vue-ssr-server-bundle.json done');
        update();
    });

    return readyPromise;
};
